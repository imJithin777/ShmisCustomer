import UIKit

struct FontHelper {
    
static func defaultRegularFontWithSize(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Regular", size: size)!
}

static func defaultBoldFontWithSize(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Bold", size: size)!
}
    
    static func defaultItalicFontWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Italic", size: size)!
    }
    
}
